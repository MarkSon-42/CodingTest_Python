n = int(input())

seq_a = list(map(int, input().split()))
seq_b = list(map(int, input().split()))


for i in range(len(seq_a) - 1, -1, -1):
    if seq_a[i] % 2 == 0:
        print(seq_a, end=' ')
for i in range(len(seq_b) - 1, -1, -1):
    if seq_b[i] % 2 != 0:
        print(seq_b, end=' ')
